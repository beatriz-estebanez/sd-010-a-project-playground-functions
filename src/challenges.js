function splitSentence(phrase) {
  let str = [];
  let word = '';
  for (let index = 0; index <= phrase.length; index += 1) {
    if (phrase[index] === ' ' || index === phrase.length) {
      str.push(word);
      word = '';
    } else {
      word += phrase[index];
    }
  }
  return str;
}

